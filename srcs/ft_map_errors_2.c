#include "cub3d.h"

int		ft_is_floor(char c)
{
	if (c == '0' || c == '2' || c == 'N' || c == 'W'
	|| c == 'E' || c == 'S')
		return (1);
	return (0);
}

int		ft_is_map(char c)
{
	if (c == '0' || c == '2' || c == 'N' || c == 'W'
	|| c == 'E' || c == 'S' || c == '1' || c == ' ')
		return (1);
	return (0);
}
